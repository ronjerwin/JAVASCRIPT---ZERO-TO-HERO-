// WHAT IS MATH OBJECT
// >> MATH OBJECT IS A BUILT-IN JAVASCRIPT METHOD WHERE IT PROVIDES A COLLECTION OF MULTIPLE PROPERTIES AND METHODS THAT CAN USE TO CALCULATE SPECIFIC TASK.


// IT HAS ALOT OF METHOD WE CAN USE TO OUR SPECIFIC NEEDS:

let number = 7.25;

let rounded = Math.round(number); // Rounds to the nearest integer
let floored = Math.floor(number); // Rounds down to the nearest integer
let ceiled = Math.ceil(number); // Rounds up to the nearest integer
let squareRoot = Math.sqrt(16); // Calculates the square root
let power = Math.pow(2, 3); // Calculates 2 raised to the power of 3
let randomNum = Math.random(); // Generates a random number between 0 and 1
let absolute = Math.abs(-10); // Returns the absolute value
let max = Math.max(5, 10, 15); // Returns the maximum value
let min = Math.min(5, 10, 15); // Returns the minimum value
let trucated = Math.trunc(4.9); // Removes the decimal part
let sine = Math.sin(Math.PI / 2); // Calculates the sine of an angle in radians
let cosine = Math.cos(0); // Calculates the cosine of an angle in radians
let tangent = Math.tan(Math.PI / 4); // Calculates the tangent of an angle in radians
let logarithm = Math.log(10); // Calculates the natural logarithm (base e)
let exponential = Math.exp(2); // Calculates e raised to the power of 2
let cbrt = Math.cbrt(27); // Calculates the cube root
let hypot = Math.hypot(3, 4); // Calculates the hypotenuse of a right triangle

console.log("Rounded:", rounded); // Output: 7
console.log("Floored:", floored); // Output: 7
console.log("Ceiled:", ceiled); // Output: 8
console.log("Square Root:", squareRoot); // Output: 4
console.log("Power:", power); // Output: 8
console.log("Random Number:", randomNum); // Output: Random number between 0 and 1
console.log("Absolute:", absolute); // Output: 10
console.log("Max:", max); // Output: 15
console.log("Min:", min); // Output: 5
console.log("Truncated:", trucated); // Output: 4
console.log("Sine:", sine); // Output: 1
console.log("Cosine:", cosine); // Output: 1
console.log("Tangent:", tangent); // Output: 1
console.log("Logarithm:", logarithm); // Output: 2.302585092994046
console.log("Exponential:", exponential); // Output: 7.3890560989306495
console.log("Cube Root:", cbrt); // Output: 3
console.log("Hypotenuse:", hypot); // Output: 5 

