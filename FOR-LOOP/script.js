// WHAT IS A FOR LOOP?
// >> A CONTROL FLOW STATEMENT THAT REPEATEDLY EXECUTES A BLOCK OF CODE AS LONG AS THE SPECIFIED CONDITION IS TRUE.
// >> IT IS HIGHLY EFFICIENT FOR ITERATING OVER A KNOWN NUMBER OF TIMES OR THROUGH DATA COLLECTIONS.

// >> SYNTAX:
//          for(initialization; condition; incrementation){ do some code }

// EXAMPLE:

  for(let i = 1; i <= 10; i++) {
    console.log(`Count: ${i}`);
  }